#loops are used for sequensial treversal. for treversing list, string, tuples, etc.

#FOR LOOPS
list = [1,2,3,4,5]
for val in list:
    print(val)

tuple = (1,2,3,4,5)
for num in tuple:
    print(num)

str = "arshan"
for char in str:
    print(char)

#loops with else
