#escape sequence characters
# {"\n" is used to print the string in new line}

str1 = "My name is Arshan.\nI am 19 years old." #\n
print(str1)

# {"\t" is used to print the string in tab space}
str2 = "My name is Arshan.\tI am 19 years old." #\t
print(str2)

#basic operations on strings
# 1) concatenation
str1 = "Hello"
str2 = " World"
str3 = str1 + str2
print(str3)

# 2) length of string
# len() is used to find the length of the string,
# it counts the number of characters in the string including spaces and special characters.
str1 = "Hello World"
print(str1)
print(len(str1))